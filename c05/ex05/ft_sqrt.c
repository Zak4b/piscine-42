int	ft_sqrt(int nb)
{
	int	i;
	int	pow;

	if (nb < 1)
		return (0);
	else if (nb == 1)
		return (1);
	i = 1;
	pow = 0;
	while (pow < nb)
	{
		i++;
		pow = i * i;
	}
	if (pow == nb)
		return (i);
	else
		return (0);
}
